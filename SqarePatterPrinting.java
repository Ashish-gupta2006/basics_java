    //  sqare patter printing in java using to loop

public class SqarePatterPrinting {
    public static void main(String[] args){
        for(int i=0 ; i<=4 ; i++){
            for(int j=0 ; j<=4 ; j++){
                System.out.print(" * ");
            }
            System.out.print("\n");
        }
    }
}
